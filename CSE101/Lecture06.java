        //GENERATING RANDOM CHARACTERS
        /**
         * All chars have a hexadecimal value between 0 and FFFF or decimal value between 0 and 65535.
         * So we can obtain a random char using Math.random() method..
         */
        char ch = (char) ((int)(Math.random()*65536));
        System.out.println(ch);
        /**
         * or we can obtain a lowercase alphabetic char using:
         */
        char ch1 = (char) ((((int)'z'-(int)'a'+1))*Math.random()+'a');
        System.out.println(ch1);
